//while
let index = 0
while (index <= 10) {
    console.log(`value of index is ${index}`);
    index = index + 2
}

let myArray = ["Batman", "Ironman", "Superman"]
let i = 0

while (i < myArray.length) {
    console.log(myArray[i]);
    i = i + 1
}

//do while

let score = 11

do {
    console.log(`score is: ${score}`);
    score = score + 1
} while (score <= 10);